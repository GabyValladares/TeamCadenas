    public static boolean validarCorreoElectronico(String correo) {
        // Expresión regular
        String rango = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
        // Transforma la expresión original, para que pueda comparar
        Pattern pattern = Pattern.compile(rango);
        // Verifica
        Matcher matcher = pattern.matcher(correo);
        // Verificar si el correo coincide con la expresión regular
        return matcher.matches();
    }
    
    public static boolean validarCedula(String cedula){
        String rango="[0-9]{10}$";
        // Transforma la expresión original, para que pueda comparar
        Pattern pattern = Pattern.compile(rango);
        // Verifica
        Matcher matcher = pattern.matcher(cedula);
        // Verificar si el correo coincide con la expresión regular
        return matcher.matches();
    }
    
    

