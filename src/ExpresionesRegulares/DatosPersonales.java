    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        // TODO add your handling code here:
        String correo = txtCorreo.getText();
        String cedula = txtCedula.getText();
        String celular = txtCelular.getText();
        String clave = txtClave.getText();
        lblRcedula.setText("");
        lblRcelular.setText("");
        lblRclave.setText("");
        lblRcorreo.setText("");

        if (!this.validarEmail(correo)) {
            lblRcorreo.setText("ERROR: El formato del correo electrónico no es válido.");
        }

        if (!this.validarCedula(cedula)) {
            lblRcedula.setText("ERROR: La cédula debe contener exactamente 10 dígitos numéricos.");
        }

        if (!this.validarCelular(celular)) {
            lblRcelular.setText("ERROR: El número de celular debe contener exactamente 10 dígitos numéricos.");
        }

        if (!this.validarClave(clave)) {
            lblRclave.setText("ERROR: La clave debe tener al menos 8 caracteres, incluyendo al menos una letra y un número.");
        }

    }//GEN-LAST:event_btnEnviarActionPerformed

