    private void btnContarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContarActionPerformed
        int conTotal= 0;
        int conA=0;
        int conE=0;
        int conI=0;
        int conO=0;
        int conU=0;
        String cadena=txtOracion.getText();
        String cambio$=cadena.replace("o", "$");
        String cambioA=cadena.replace("a", "@");
        for (int i = 0; i < cadena.length(); i++) {
            String letra=cadena.charAt(i)+"";
            if(letra.equalsIgnoreCase("a")){
                conA++;
            }else if(letra.equalsIgnoreCase("e")){
                conE++;
            }else if(letra.equalsIgnoreCase("i")){
                conI++;
            }else if(letra.equalsIgnoreCase("o")){
                conO++;
            }else if(letra.equalsIgnoreCase("u")){
                conU++;
            }
        }
        conTotal=conA+conE+conI+conO+conU;
        System.out.println(cambio$+"\n"+cambioA);
    }//GEN-LAST:event_btnContarActionPerformed

