    private boolean validarEmail(String email) {
        String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
        return email.matches(emailRegex);

    }

    private boolean validarCedula(String cedula) {
        String cedulaRegex = "^\\d{10}$";
        return cedula.matches(cedulaRegex);
    }

    private boolean validarCelular(String celular) {
        String celularRegex = "^\\d{10}$";
        return celular.matches(celularRegex);
    }

    private boolean validarClave(String clave) {
        String claveRegex = "^(?=.*[A-Za-z])(?=.*\\d)[A-Za-z\\d]{8,}$";
        return clave.matches(claveRegex);
    }

