    private void BttnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BttnConfirmarActionPerformed
        // TODO add your handling code here:
        String correo= txtCorreo.getText();
        int cedula=  Integer.parseInt(txtCedula.getText());
        int celular = Integer.parseInt(txtCelular.getText());
        String clave= txtClave.getText();

        //Validar Correo
        if (validarCorreo(correo)) {
        JOptionPane.showMessageDialog(null, "SU CORREO ES:"+correo);
    } else {
        // Correo no válido, muestra un mensaje de error
        JOptionPane.showMessageDialog(this, "Correo electrónico no válido", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    // Validar Cédula
        if (validarCedula(cedula)) {
            // Cédula válida, puedes continuar
                JOptionPane.showMessageDialog(null, "SU CEDULA ES:"+cedula);
        } else {
            // Cédula no válida, muestra un mensaje de error
            JOptionPane.showMessageDialog(this, "Cédula no válida. Debe contener 10 dígitos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    // Validar Celular
        if (validarCelular(celular)) {
            // Celular válido, puedes continuar
            JOptionPane.showMessageDialog(null, "SU CELULAR ES:"+celular);
        } else {
            // Celular no válido, muestra un mensaje de error
            JOptionPane.showMessageDialog(this, "Número de celular no válido. Debe contener 10 dígitos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } 
    // Validar Clave segura
    if (validarClave(clave)) {
        // Clave válida, puedes continuar
        JOptionPane.showMessageDialog(null, "SU CLAVE ES:"+"************");
    } else {
        // Clave no válida, muestra un mensaje de error
        JOptionPane.showMessageDialog(this, "Clave no válida. Debe tener al menos 8 caracteres, incluyendo letras mayúsculas, minúsculas y números.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
}

// Función para validar Cédula
private boolean validarCedula(int cedula) {
    String cedulaStr = String.valueOf(cedula);
    return cedulaStr.matches("\\d{10}");
}

// Función para validar Celular
private boolean validarCelular(long celular) {
    String celularStr = String.valueOf(celular);
    return celularStr.matches("\\d{9,10}");
}

// Función para validar Clave segura
private boolean validarClave(String clave) {
    return clave.matches("^(?=.*[a-z])(?=.*[A-Z])(?=.*\\d).{8,}$");
}

// Función para validar Correo electrónico
private boolean validarCorreo(String correo) {
    String regex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
    Pattern pattern = Pattern.compile(regex);
    Matcher matcher = pattern.matcher(correo);
    return matcher.matches();
    }//GEN-LAST:event_BttnConfirmarActionPerformed

