    private void btComprobarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btComprobarActionPerformed
        // TODO add your handling code here:
        String Cedula=txtCedula.getText();
        String Correo=txtCorreoE.getText();
        String Celular=txtCelular.getText();
        String ClaveS=txtClaveS.getText();
       
        if( Correo.isEmpty()|| Celular.isEmpty() || ClaveS.isEmpty() ||Cedula.isEmpty() ){
            JOptionPane.showMessageDialog(rootPane, "ERROR, DATOS VACIOS.");
            lbCedula.setForeground(Color.red);
            lbCorreoE.setForeground(Color.red);
            lbCelular.setForeground(Color.red);
            lbClaveS.setForeground(Color.red);
            txtCedula.setBackground(Color.red);
            txtCorreoE.setBackground(Color.red);
            txtCelular.setBackground(Color.red);
            txtClaveS.setBackground(Color.red);   
        }else{
            lbCedula.setForeground(Color.WHITE);
            lbCorreoE.setForeground(Color.WHITE);
            lbCelular.setForeground(Color.WHITE);
            lbClaveS.setForeground(Color.WHITE);
            txtCedula.setBackground(Color.WHITE);
            txtCorreoE.setBackground(Color.WHITE);
            txtCelular.setBackground(Color.WHITE);
            txtClaveS.setBackground(Color.WHITE); 
        }
        try {
            //VALIDACION DEL CORREO ELECTRONICO
            //AquÃ­ se utiliza el mÃ©todo estÃ¡tico compile de la clase Pattern para compilar la expresiÃ³n regular
            //[a-z0-9]:Esto especifica que la cadena debe comenzar con uno o mÃ¡s caracteres alfanumÃ©ricos en minÃºsculas.
            //[\\._]?: AquÃ­, se permite un carÃ¡cter de punto (.) o guion bajo (_) opcional despuÃ©s de la primera parte.
            //[a-z0-9]+: DespuÃ©s del punto o guion bajo opcional, se espera otra secuencia de uno o mÃ¡s caracteres alfanumÃ©ricos en minÃºsculas.
            //[.]: Asegura que haya un punto despuÃ©s del dominio.
            //\\w{2,3}$: Esta parte indica que al final de la cadena deben haber entre 2 y 3 caracteres de palabra.
            Pattern patternCorreo=Pattern.compile("^[a-z0-9]+[\\._]?[a-z0-9]+[@]\\w+[.]\\w{2,3}$");
            //realiza operaciones de coincidencia de patrones en una cadena.
            Matcher matcherCorreo = patternCorreo.matcher(Correo);
            if(matcherCorreo.matches()){
                lbMesCorreoE.setText("Correo VÃ¡lido");
                //JOptionPane.showMessageDialog(rootPane, "Correo VÃ¡lido");
                lbCorreoE.setForeground(Color.GREEN);
            }else{
                lbMesCorreoE.setText("Correo InvÃ¡lido");
                //JOptionPane.showMessageDialog(rootPane,"Correo InvÃ¡lido");
                lbCorreoE.setForeground(Color.red);
            }
            //VALIDACION DE LA CEDULA
            //^: Indica el inicio de la cadena. 
            //d{10}: Representa exactamente 10 dÃ­gitos numÃ©ricos consecutivos
            //$: Indica el final de la cadena.
            Pattern patternCedula=Pattern.compile("^\\d{10}$");
            Matcher matcherCedula = patternCedula.matcher(Cedula);
            if(matcherCedula.matches()){
                lbMesCedula.setText("Cedula VÃ¡lida");
                lbCedula.setForeground(Color.GREEN);
            }else{
                lbMesCedula.setText("Cedula InvÃ¡lida");
                lbCedula.setForeground(Color.red);
            }
            
            //VALIDAR TELEFONO CELULAR
            Pattern patternCelular=Pattern.compile("^\\d{10}$");
            Matcher matcherCelular = patternCelular.matcher(Celular);
            if(matcherCelular.matches()){
                lbMesTelefonoCe.setText("Celular VÃ¡lido");
                //JOptionPane.showMessageDialog(rootPane, "Celular VÃ¡lido");
                lbCelular.setForeground(Color.GREEN);
            }else{
                //JOptionPane.showMessageDialog(rootPane,"Celular InvÃ¡lido");
                lbMesTelefonoCe.setText("Celular VÃ¡lido");
                lbCelular.setForeground(Color.red);
            }
            
            //VALIDAR CLAVE
            //(?=.*[a-z]): verifica que haya una letra miniscula
            //(?=.*[A-Z]): verifica que haya una letra mayuscula
            //(?=.*\d); verifica que haya almenos un numero
            //[a-zA-Z\d]{10,}: veifica que haya en tenga 9 caracteres
            //$: indica el final de la cadena 
            Pattern patternClaveS=Pattern.compile("^(?=.[a-z])(?=.[A-Z])(?=.*\\d)[a-zA-Z\\d]{8}$'");
            Matcher matcherClaveS = patternClaveS.matcher(ClaveS);
            if(matcherClaveS.matches()){
                lbMesClaveS.setText("CLAVE SEGURA");
                lbClaveS.setForeground(Color.GREEN);
            }else{
                lbMesClaveS.setText("CLAVE INSEGURA");
                lbClaveS.setForeground(Color.red);
            }
            
        } catch (Exception e) {
             System.out.println("Error: ");
        }
        txtCedula.setText("");
        txtCorreoE.setText("");
        txtCelular.setText("");
        txtClaveS.setText("");
        
    }//GEN-LAST:event_btComprobarActionPerformed

