    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        //ingresar un correo electronico 
       String correo = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
       
     //ingresar 10 dijitos
       String cedula = "\\d{10}";
       //ingresar 10 digitos
       String celular = "\\d{10}";
      // ingresar numeros letastas en mayusculas y minusculas yalmenos un caracter la contrasenia puede ser de 8 a 16 caracteres
       String clave = "^(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z])(?=.*[@#$%^&+=])(?=\\S+$).{8,16}$";

       String correo1 = txtcorreo.getText();
       String cedula1 = txtcedula.getText();
       String celular1 = txtcelular.getText();
       String clave1 = txtclave.getText();

           // Validación del correo electrónico
           
            if (correo1.matches(correo)) {
                
              lblcorr.setText("CORREO VALIDO");
              
              lblcorr.setForeground(Color.GREEN);
              
              } else {
                
                lblcorr.setText("CORREO NO VALIDO");
                
                 lblcorr.setForeground(Color.RED);
                 
                }
            

               // Validación de la cédula
               
               if (cedula1.matches(cedula)) {
                   
              lblced.setText("CEDULA VALIDA");
              
               lblced.setForeground(Color.GREEN);
               
         } else {
                   
           lblced.setText("NUMERO DE CEDULA NO VALIDO");
           
              lblced.setForeground(Color.RED);
              }

            // Validación del número de celular
            if (celular1.matches(celular)) {
                
             lblcel.setText("NUMERO VALIDO");
             
             lblcel.setForeground(Color.GREEN);
             
          } else {
                
            lblcel.setText("NUMERO CELULAR NO VALIDO");
            
             lblcel.setForeground(Color.RED);
                 }

           // Validación de la contraseña
              if (clave1.matches(clave)) {
                  
               lblclav.setText("CLAVE VALIDA");
               
                lblclav.setForeground(Color.GREEN);
                
           } else {
                  
               lblclav.setText("CLAVE NO VALIDA");
               
                 lblclav.setForeground(Color.RED);
                     }

    
    }//GEN-LAST:event_btnguardarActionPerformed

