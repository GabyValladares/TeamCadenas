    private void validarDatos(){
    String correo=txtIngresoCorreo.getText();
    String cedula=txtIngresoCedula.getText();
    String celular=txtIngresoCelular.getText();
    String clave=txtIngresoClave.getText();
    if (validarCorreo(correo) && validarCedula(cedula) && validarCelular(celular) && validarClave(clave)) {
            JOptionPane.showMessageDialog(this, "Datos validos. ¡Registro exitoso");
        } else {
            JOptionPane.showMessageDialog(this, "Datos invalidos. Verifica la informacion ingresada.");
        }
    }
    
    
    private boolean validarCorreo(String correo) {
        String regexCorreo = "^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Z|a-z]{2,}$";
        Pattern pattern = Pattern.compile(regexCorreo);
        Matcher matcher = pattern.matcher(correo);
        return matcher.matches();
    }
    
    
    private boolean validarCedula(String cedula) {
        String regexCedula = "^[0-9]+$";
        Pattern pattern = Pattern.compile(regexCedula);
        Matcher matcher = pattern.matcher(cedula);
        return matcher.matches();
    }
    
    
    private boolean validarCelular(String celular) {
        String regexCelular = "^[0-9]+$";
        Pattern pattern = Pattern.compile(regexCelular);
        Matcher matcher = pattern.matcher(celular);
        return matcher.matches();
    }
    
    
     private boolean validarClave(String clave) {
        String regexClave = "^(?=.*[A-Z])(?=.*\\d).{8,}$";
        Pattern pattern = Pattern.compile(regexClave);
        Matcher matcher = pattern.matcher(clave);
        return matcher.matches();
     }
