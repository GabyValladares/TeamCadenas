    private void contarEspacios (String cadena){
        
        int contador = 0;
        for(int i = 0; i < cadena.length(); i++){ //El m√©todo length devuelve la longitud o el n√∫mero de caracteres de la cadena almacenada en la variable
            if(cadena.charAt(i) == ' '){// El m√©todo charAt indica que siempre que tenga una cadena se puede ir separando en car√©cteres (char)
                                                //Se utiliza para obtener el car√°cter en la posici√≥n i de la cadena.
                contador ++; 
            }
        }
        
        JOptionPane.showMessageDialog(this, "La frase tiene "+ contador + " espacios en blanco." );
    }
